class Calculator():

    # Multiplication
    def multiply(self, p_val1: int, p_val2: int) -> int:
        return p_val1 * p_val2

    # substraction
    def substract(self, p_val1: int, p_val2: int) -> int:
        return p_val1 - p_val2

    # addition
    def add(self, p_val1: int, p_val2: int) -> int:
        return p_val1 + p_val2

    # addition
    def divide(self, p_val1: int, p_val2: int) -> int:
        return p_val1 / p_val2
